package com.cube.model.dto;

public class HappyDayDto {

	 private String happy_code;
	 private String employee_id;
	 private String happy_name;
	 private String happy_stat;
	 private String happy_end;
	 
	 
	public String getHappy_code() {
		return happy_code;
	}
	public void setHappy_code(String happy_code) {
		this.happy_code = happy_code;
	}
	public String getEmployee_id() {
		return employee_id;
	}
	public void setEmployee_id(String employee_id) {
		this.employee_id = employee_id;
	}
	public String getHappy_name() {
		return happy_name;
	}
	public void setHappy_name(String happy_name) {
		this.happy_name = happy_name;
	}
	public String getHappy_stat() {
		return happy_stat;
	}
	public void setHappy_stat(String happy_stat) {
		this.happy_stat = happy_stat;
	}
	public String getHappy_end() {
		return happy_end;
	}
	public void setHappy_end(String happy_end) {
		this.happy_end = happy_end;
	}
	 
}
